# A scatter plot is used to display the relationship
# between two numerical variables, and plots one dot 
# for each observation.


# it needs two vectors of same length, one for the 
# x-axis and  one for the y-axis.

x <- c(5,7,8,7,2,2,9,4,11,12,9,6)
y <- c(99,86,87,88,111,103,87,94,78,77,85,86)

plot(x, y, main="Observations of cars", xlab="car age", ylab="car speed")


